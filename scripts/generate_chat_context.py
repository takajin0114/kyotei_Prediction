#!/usr/bin/env python3
"""
ChatGPT レビュー用の統合サマリ docs/ai_dev/chat_context.md を自動生成する。
Python標準ライブラリのみ使用。リポジトリルートを自動判定。毎回上書き・UTF-8保存。
"""

import re
from datetime import datetime
from pathlib import Path
from typing import Optional, Tuple


def find_repo_root(start: Path) -> Path:
    """.git があるディレクトリをリポジトリルートとする。"""
    current = start.resolve()
    while current != current.parent:
        if (current / ".git").exists():
            return current
        current = current.parent
    return start


def read_file(root: Path, rel_path: str) -> str:
    """リポジトリルート基準のパスでファイルを読み、内容を返す。なければ (not found)。"""
    p = root / rel_path
    if not p.is_file():
        return "(not found)"
    try:
        return p.read_text(encoding="utf-8").strip()
    except OSError:
        return "(read error)"


def choose_latest_experiment_log(logs_dir: Path) -> Tuple[Optional[Path], str]:
    """
    experiments/logs/*.md のうち最新1件を選ぶ。
    優先順位: 1) EXP-xxxx の番号が最大 2) 番号が取れない場合は更新日時が最新。
    戻り値: (採用したファイルの Path or None, 採用したファイル名)
    """
    if not logs_dir.is_dir():
        return None, ""
    md_files = list(logs_dir.glob("*.md"))
    if not md_files:
        return None, ""

    exp_pattern = re.compile(r"EXP-(\d+)", re.IGNORECASE)

    def key(f: Path) -> tuple[int, float]:
        stem = f.stem
        m = exp_pattern.search(stem)
        if m:
            return (int(m.group(1)), -f.stat().st_mtime)
        return (-1, -f.stat().st_mtime)

    chosen = max(md_files, key=key)
    return chosen, chosen.name


def main() -> int:
    script_path = Path(__file__).resolve()
    root = find_repo_root(script_path)
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    repo_name = root.name

    run_report_path = root / "docs" / "ai_dev" / "run_report.md"
    leaderboard_path = root / "experiments" / "leaderboard.md"
    project_status_path = root / "docs" / "ai_dev" / "project_status.md"
    logs_dir = root / "experiments" / "logs"

    run_report_content = read_file(root, "docs/ai_dev/run_report.md")
    leaderboard_content = read_file(root, "experiments/leaderboard.md")
    project_status_content = read_file(root, "docs/ai_dev/project_status.md")

    latest_path, latest_name = choose_latest_experiment_log(logs_dir)
    if latest_path:
        try:
            latest_content = latest_path.read_text(encoding="utf-8").strip()
        except OSError:
            latest_content = "(read error)"
    else:
        latest_content = "(not found)"
        latest_name = "(none)"

    out_lines = [
        "# Chat Context",
        "",
        "## Generated At",
        timestamp,
        "",
        "## Repository",
        repo_name,
        "",
        "## Run Report",
        "",
        run_report_content if run_report_content != "(not found)" else "(not found)",
        "",
        "## Leaderboard",
        "",
        leaderboard_content if leaderboard_content != "(not found)" else "(not found)",
        "",
        "## Project Status",
        "",
        project_status_content if project_status_content != "(not found)" else "(not found)",
        "",
        "## Latest Experiment",
        "",
        latest_content,
        "",
        "## Notes",
        "",
        f"- latest experiment file: {latest_name}",
        "- generated by: scripts/generate_chat_context.py",
        "",
    ]

    out_path = root / "docs" / "ai_dev" / "chat_context.md"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(out_lines), encoding="utf-8")
    print(f"Generated {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
